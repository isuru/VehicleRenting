    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        System.out.println("Save mode: " + saveMode);
        
        String un, pw, rpw;
        List<String> messages = new ArrayList<>();

        un = txtUsername.getText();
        pw = new String(txtPassword.getPassword());
        rpw = new String(txtRetypePassword.getPassword());
        
        if ( un.equals("") ) { messages.add("Username can't be blank"); txtUsername.grabFocus(); }
        if ( pw.equals("") ) { messages.add("Password can't be blank"); txtPassword.grabFocus(); }
        if ( rpw.equals("") ) { messages.add("Retype Password can't be blank"); txtRetypePassword.grabFocus(); }
        if ( !rpw.equals(pw) ) { messages.add("Passwords do not match"); txtRetypePassword.grabFocus(); }
        if ( !pw.equals("") && !rpw.equals("") && pw.length() < 6 ) { messages.add("Password is too short. Please enter at least 6 characters"); txtPassword.grabFocus(); }

        if (messages.size() > 0) {
            JOptionPane.showConfirmDialog(this, "The following error(s) occurred when adding the user: \n\n" + String.join("\n", messages), "User Management", JOptionPane.DEFAULT_OPTION);

        } else {
            if (UserController.addNewUser(un, pw, rpw)) {
                this.listUsers();
                JOptionPane.showConfirmDialog(this, "New user created successfully", "Add User", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                lstUsers.setSelectedIndex(lstUsers.getModel().getSize()-1);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

