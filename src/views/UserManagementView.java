    private void btnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePasswordActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Are you sure, you want to change password of the following user?\n\nUsername: " + txtUsername.getText(), "Change Password", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
            JPasswordField pwdField = new JPasswordField();
            int retval = JOptionPane.showConfirmDialog(null, pwdField, "Enter new password\n(Minimum length: 4 characters)", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

            if (retval == JOptionPane.OK_OPTION) {
                String newPassword = new String(pwdField.getPassword());

                if (newPassword.length() == 0) {
                    JOptionPane.showConfirmDialog(this, "Failed to change password.\n\nPassword can't be blank", "Change Password", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
                    return;
                    
                } else if (newPassword.length() < 4) {
                    JOptionPane.showConfirmDialog(this, "Failed to change password.\n\nPassword is too short!", "Change Password", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
                    return;                    
                }

                if (UserController.changePassword(txtUsername.getText(), newPassword)) {
                    JOptionPane.showConfirmDialog(this, "Password changed successfully", "Change Password", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }        
    }//GEN-LAST:event_btnChangePasswordActionPerformed

